// Math Object

console.log(Math.sqrt(25), "<- SquareRoot of 25")
console.log(Math.abs(-5), "<- Absolute value of -5")
// console.log(Math.sin(1),"<- Absolute value")
console.log(Math.pow(2, 3), "<- 2 to the power 3")
console.log(Math.floor(2.3), "<- Floor round value of 2.3")
console.log(Math.ceil(2.3), "<- Upper round value of 2.3")
console.log(Math.round(2.3), "<- Round value of 2.3")
console.log(Math.round(2.5), "<- Round value of 2.5")
console.log(Math.max(2, 5, 6, 8, -5), "<- Max value of (2,5,6,8,-5)")
console.log(Math.min(2, 5, 6, 8, -5), "<- Min value of (2,5,6,8,-5)")

console.log(Math.random(), "<- Random value between 0 to 1")
console.log(Math.random() * 10, "<- Random value between 0 to 10")
console.log((Math.random() * 9)+1, "<- Random value between 1 to 10")
console.log((Math.random() * 11)+10, "<- Random value between 10 to 20")